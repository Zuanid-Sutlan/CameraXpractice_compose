@OptIn(ExperimentalMaterial3Api::class)
class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

        // checking self permission
        if (!hasPermissionRequired()) {
            requestPermissions()
        }

        setContent {
            val viewModel: MainViewModel = viewModel()
            val navController = rememberNavController()
            CameraXpractice_composeTheme {
                // A surface container using the 'background' color from the theme
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background
                ) {
                    Navigation(viewModel = viewModel, navController = navController)
                }
            }
        }
    }





    private fun requestPermissions() {
        ActivityCompat.requestPermissions(
            this,
            PERMISSIONS,
            0 // request code for permission
        )
    }

    private fun hasPermissionRequired(): Boolean {
        return PERMISSIONS.all {
            ContextCompat.checkSelfPermission(
                applicationContext,
                it
            ) == PackageManager.PERMISSION_GRANTED
        }
    }

    companion object {
        private val PERMISSIONS = arrayOf(
            Manifest.permission.CAMERA
        )
    }
}



